package com.example.myapplication.ui.home

import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import androidx.fragment.app.Fragment
import androidx.lifecycle.ViewModelProvider
import com.example.myapplication.databinding.FragmentMyScheduleBinding

class MyScheduleFragment : Fragment() {

  private lateinit var myScheduleViewModel: MyScheduleViewModel
private var _binding: FragmentMyScheduleBinding? = null
  // This property is only valid between onCreateView and
  // onDestroyView.
  private val binding get() = _binding!!

  override fun onCreateView(
    inflater: LayoutInflater,
    container: ViewGroup?,
    savedInstanceState: Bundle?
  ): View? {
    myScheduleViewModel =
            ViewModelProvider(this).get(MyScheduleViewModel::class.java)

    _binding = FragmentMyScheduleBinding.inflate(inflater, container, false)
    val root: View = binding.root


    return root
  }

override fun onDestroyView() {
        super.onDestroyView()
        _binding = null
    }
}